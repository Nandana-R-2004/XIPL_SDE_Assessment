import numpy as np

def check_guardrails(person_img):

    # Detect people
    results = yolo.predict(person_img, verbose=False)

    person_found = False

    for box in results[0].boxes:
        cls = int(box.cls[0])

        # COCO class 0 = person
        if cls == 0:
            person_found = True

            x1, y1, x2, y2 = box.xyxy[0].tolist()

            w = x2 - x1
            h = y2 - y1

            # Very simple pose heuristic
            if w > h:
                return True, "⚠️ Warning: Side pose detected."

            if h / w < 1.4:
                return True, "⚠️ Warning: Person may be seated."

            return True, "✅ Suitable pose."

    return False, "❌ No person detected."

