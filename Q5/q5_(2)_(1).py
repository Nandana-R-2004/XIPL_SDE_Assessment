def run_virtual_tryon(person_img, garment_img):

    start = time.time()

    # Save temporary images
    person_path = tempfile.mktemp(suffix=".png")
    garment_path = tempfile.mktemp(suffix=".png")

    person_img.save(person_path)
    garment_img.save(garment_path)

    # -----------------------------
    # Guardrails
    # -----------------------------
    ok, message = check_guardrails(person_img)

    if not ok:
        return None, message, "", "", ""

    # -----------------------------
    # Generate Mask
    # -----------------------------
    mask_dict = auto_masker(person_img, "upper")

    mask = mask_dict["mask"]

    # -----------------------------
    # Run CatVTON
    # -----------------------------
    result = pipeline(
        image=person_img,
        condition_image=garment_img,
        mask=mask,
        num_inference_steps=30,
        guidance_scale=2.5,
        height=1024,
        width=768,
    )

    # Some versions return a list, others return a PIL image
    if isinstance(result, list):
        result = result[0]

    output_path = tempfile.mktemp(suffix=".png")
    result.save(output_path)

    # -----------------------------
    # Evaluation
    # -----------------------------
    garment = clip_similarity(
        garment_path,
        output_path
    )

    identity = identity_score(
        person_path,
        output_path
    )

    score, reason = judge_score(
        garment,
        identity
    )

    scores = f"""
Garment Fidelity : {garment:.4f}

Identity Preservation : {identity:.4f}

Judge Score : {score}/10

Reason:
{reason}
"""

    processing = f"{time.time()-start:.2f} sec"

    garment_attributes = """
Garment Attributes (Q1)

• Category : Upper Wear
• Color : Auto
• Pattern : Auto
• Sleeve : Auto

(Placeholder for Q1 output)
"""

    return (
        result,
        message,
        garment_attributes,
        scores,
        processing,
    )

